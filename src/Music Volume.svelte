<script>
	import { persist } from './shared.svelte';
	import { _sound } from './sound.svelte';

	const onClick = (i) => {
		_sound.music = i;
		_sound.playMusic();

		persist(true);
	};
</script>

<div class="selector">
	<div class="label">Music</div>
	<div class="values">
		{#each [0, 1, 2, 3] as i (i)}
			<div class="value {i === _sound.music ? 'selected' : ''}" onpointerdown={() => onClick(i)}>{i}</div>
		{/each}
	</div>
</div>

